#include <stdio.h>

int main(void){
    int x = 21;
    int y = 10;
    printf("x: %d y: %d\n",x,y);

    printf("x + y = : %d\n",x + y);
    printf("x - y = : %d\n",x - y);
    printf("x * y = : %d\n",x * y);
    printf("x / y = : %d  x %% y = %d\n",x / y, x % y);

    return 0;
}
